let free debug ev = 
  match getIov ev with 
    | None -> ()
    | Some iovl -> Iovecl.free iovl

let copy debug ev = 
  match getIov ev with
    | None -> ev
    | Some iovl -> 
	ignore (Iovecl.copy iovl) ;
	ev 

