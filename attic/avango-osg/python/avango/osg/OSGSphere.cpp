#include <boost/python.hpp>
#include <avango/python/register_field.h>
#include <avango/osg/Sphere.h>
#include "OSGSphere.h"

using namespace boost::python;
using namespace av::python;

namespace boost
{
  namespace python
  {
    template <class T> struct pointee<av::Link<T> >
    {
      typedef T type;
    };
  }
}

void init_OSGSphere(void)
{
  // wrapping osg::Sphere functionality
  register_field<av::osg::SFSphere>("SFSphere");
  register_multifield<av::osg::MFSphere>("MFSphere");
  class_<av::osg::Sphere, av::Link<av::osg::Sphere>, bases<av::osg::MatrixTransform>, boost::noncopyable >("Sphere", "docstring", no_init);

}
